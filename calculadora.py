print("Calculadora Simples")

n1 = float(input("Digite o primeiro número: "))
n2 = float(input("Digite o segundo número: "))

print("1 - Soma")
print("2 - Subtração")
print("3 - Multiplicação")
print("4 - Divisão")

op = input("Escolha a operação: ")

if op == "1":
    print("Resultado:", n1 + n2)
elif op == "2":
    print("Resultado:", n1 - n2)
elif op == "3":
    print("Resultado:", n1 * n2)
elif op == "4":
    print("Resultado:", n1 / n2)
else:
    print("Opção inválida")
